//Write program to perform addition of two numbers
#include<stdio.h>


int main()
{
	int i=0,j=0,k=0;
	
	printf("Enter the first number:\n");
	scanf("%d",&i);
	
	printf("Enter the second number:\n");
	scanf("%d",&j);
	
	k=i+j;
	printf("Addition of two number=%d\n",k);
	
	return 0;
}